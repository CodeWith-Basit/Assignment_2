void main() {
  //Q.2: Take two variables and store age then using if/else condition to determine oldest and youngest among them.
  int age_1 = 20;
  int age_2 = 75;

  if (age_1 < age_2) {
    print("Age_1 is Younger and Age_2 is Older");
  } else if (age_1 == age_2) {
    print("Both ages are same");
  } else {
    print("Age_1 is Older and Age_2 is Younger");
  }
}
